"""
=====================================================
节点模块 (Node Module)
=====================================================
功能说明:
    该模块定义了A*算法中使用的节点类。

类说明:
    Node: A*搜索过程中的节点，存储位置、父节点、代价等信息。
=====================================================
"""


class Node:
    """
    A*算法节点类

    用于存储A*搜索过程中每个节点的信息，包括：
    - 位置坐标 (x, y, z)
    - 父节点指针（用于回溯路径）
    - 实际代价 (G cost)：从起点到当前节点的实际代价
    - 启发式代价 (H cost)：从当前节点到终点的估计代价
    - 总代价 (F cost)：G + H，用于节点选择
    """

    def __init__(self, position, parent=None):
        """
        初始化节点

        参数:
            position: tuple - 节点的3D坐标 (x, y, z)
            parent: Node - 父节点，默认为None（起点无父节点）
        """
        self.position = position        # 节点的3D网格坐标 (x, y, z)
        self.parent = parent            # 父节点指针，用于路径回溯
        self.cost = 0                   # G代价：从起点到当前节点的实际代价
        self.heuristic_cost = 0         # H代价：从当前节点到终点的启发式估计
        self.total_cost = 0             # F代价：F = G + H，用于优先级排序
